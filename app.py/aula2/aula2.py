a = int(input("Entre com o primeiro numero: "))
b = int(input('Entre com o segundo numero: '))
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
print('Soma: {} \nSubtração: {} \nMultiplicação: {} \nDivisão: {} \nResto: {}'.format(soma,
                                                                                      subtracao,
                                                                                      multiplicacao,
                                                                                      divisao,
                                                                                      resto))
